package com.tution;

public class BitwiseOperators {
	public static void main(String... args){
		int a=8,b=13;
		System.out.println("a = "+a+" b = "+b);
		System.out.println("a&b "+(a&b));
		System.out.println("a|b "+(a|b));
		System.out.println("a^b "+(a^b));
		System.out.println("a>>1 "+(a>>1));
		System.out.println("a>>2 "+(a>>2));
		System.out.println("a<<1 "+(a<<1));
	}
}
