package com.tution;

public class CharacterArithmetic {

	public static void main(String[] args) {

		char c1='A', c2='B';
		
		System.out.println(c1);
		System.out.println(c2);
		
		System.out.println("Addition "+(c1+c2));
		System.out.println("Substraction "+(c1+c2));
		System.out.println("Addition using integer"+(c1+5));
		System.out.println("Addition of character"+(c1+'5'));
	}

}
